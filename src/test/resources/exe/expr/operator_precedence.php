<?php
// Приоритет операторов
echo 2 + 3 * 5 . "\n";          // 2 + 15 = 17
echo (2 + 3) * 6 . "\n";        // 5 * 6 = 30
echo 5 * 2 / 5 - 2 . "\n";      // 10 / 5 - 2 = 2 - 2 = 0

// Логические операторы
echo (true && false || true) . "\n";  // false || true = true

// Конкатенация и сложение
echo "Hello" . 10 + 20 . "\n";  // "Hello10" + 20 = 20 (если Hello10 приводится к 0)
?>