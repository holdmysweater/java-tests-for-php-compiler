<?php
// Операторы присваивания
$a = 10;
echo $a . "\n";

$a += 10;
echo $a . "\n";

$a -= 10;
echo $a . "\n";

$a *= 4;
echo $a . "\n";

$a /= 20;
echo $a . "\n";

$a %= 2;
echo $a . "\n";

$b = 10;
$b **= 2;
echo $b . "\n";

$str = "Hello";
$str .= " World";
echo $str . "\n";

$c = 4;
$c <<= 1;
echo $c . "\n";

$d = 4;
$d >>= 1;
echo $d . "\n";
